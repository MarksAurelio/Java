/*for: Executa um bloco de código um número específico de vezes */

public class ExemploFor {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Iteração número: " + (i + 1));
            // Saída:
            // Iteração número: 1
            // Iteração número: 2
            // Iteração número: 3
            // Iteração número: 4
            // Iteração número: 5
        }
    }
}
