//boolean (booleano): Representa verdadeiro (true) ou falso (false).
public class ExemploBoolean {
    public static void main(String[] args) {
        boolean estaChovendo = false;
        boolean possuiDesconto = true;
        System.out.println("Está chovendo? " + estaChovendo); // Saída: Está chovendo? false
        System.out.println("Possui desconto? " + possuiDesconto); // Saída: Possui desconto? true
    }
}
