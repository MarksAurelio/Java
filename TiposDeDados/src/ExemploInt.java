/*1. Tipos de Dados Primitivos:

Os tipos de dados primitivos em Java representam valores básicos.

int (inteiro): Números inteiros sem casas decimais.*/

public class ExemploInt {
    public static void main(String[] args) {
        int idade = 30;
        int numeroDeAlunos = 150;
        System.out.println("Idade: " + idade); // Saída: Idade: 30
        System.out.println("Número de alunos: " + numeroDeAlunos); // Saída: Número de alunos: 150
    }
}
