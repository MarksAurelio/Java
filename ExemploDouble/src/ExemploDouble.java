//double (ponto flutuante de precisão dupla): Números com casas decimais.
public class ExemploDouble {
    public static void main(String[] args) {
        double precoProduto = 99.99;
        double alturaPessoa = 1.75;
        System.out.println("Preço do produto: R$" + precoProduto); // Saída: Preço do produto: R$99.99
        System.out.println("Altura da pessoa: " + alturaPessoa + "m"); // Saída: Altura da pessoa: 1.75m
    }
}
