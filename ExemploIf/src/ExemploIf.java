/*4. Estruturas de Controle:

Permitem controlar o fluxo de execução do seu programa, 
decidindo quais blocos de código serão executados e quantas vezes.

if: Executa um bloco de código se uma condição for verdadeira. */

public class ExemploIf {
    public static void main(String[] args) {
        int nota = 75;
        if (nota >= 70) {
            System.out.println("Aprovado!"); // Saída: Aprovado!
        }
    }
}
