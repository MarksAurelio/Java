//char (caractere): Representa um único caractere Unicode.

public class ExemploChar {
    public static void main(String[] args) {
        char primeiraLetra = 'J';
        char simbolo = '$';
        System.out.println("Primeira letra: " + primeiraLetra); // Saída: Primeira letra: J
        System.out.println("Símbolo: " + simbolo); // Saída: Símbolo: $
    }
}
