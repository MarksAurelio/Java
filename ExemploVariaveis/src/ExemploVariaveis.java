/*2. Variáveis:

Variáveis são "caixas" onde você armazena dados de um determinado tipo. 
Você precisa declarar o tipo da variável e dar um nome a ela. */

public class ExemploVariaveis {
    public static void main(String[] args) {
        int numero; // Declara uma variável inteira chamada 'numero'
        numero = 10; // Atribui o valor 10 à variável 'numero'
        System.out.println("O valor de numero é: " + numero); // Saída: O valor de numero é: 10

        String texto = "Olá novamente!"; // Declara e inicializa uma variável String
        System.out.println(texto); // Saída: Olá novamente!
    }
}
