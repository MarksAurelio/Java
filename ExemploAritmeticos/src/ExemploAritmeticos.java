/*3. Operadores:

Símbolos que realizam operações em valores e variáveis.

Aritméticos: + (adição), - (subtração), * (multiplicação), / (divisão), 
% (módulo - resto da divisão). */
public class ExemploAritmeticos {
    public static void main(String[] args) {
        int a = 15;
        int b = 7;
        System.out.println("a + b = " + (a + b));   // Saída: a + b = 22
        System.out.println("a - b = " + (a - b));   // Saída: a - b = 8
        System.out.println("a * b = " + (a * b));   // Saída: a * b = 105
        System.out.println("a / b = " + (a / b));   // Saída: a / b = 2 (divisão inteira)
        System.out.println("a % b = " + (a % b));   // Saída: a % b = 1 (resto da divisão)
    }
}
