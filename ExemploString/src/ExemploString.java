//String (cadeia de caracteres): Embora não seja um tipo primitivo, 
//é fundamental para trabalhar com texto. É uma classe em Java.

public class ExemploString {
    public static void main(String[] args) {
        String nome = "João Silva";
        String mensagem = "Bem-vindo ao Java!";
        System.out.println("Nome: " + nome); // Saída: Nome: João Silva
        System.out.println("Mensagem: " + mensagem); // Saída: Bem-vindo ao Java!
    }
}
